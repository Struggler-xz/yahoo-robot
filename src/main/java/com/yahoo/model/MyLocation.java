package com.yahoo.model;

public class MyLocation {
    MyCityEnum myCity;
    int x;
    int y;

    public MyCityEnum getMyCity() {
        return myCity;
    }

    public void setMyCity(MyCityEnum myCity) {
        this.myCity = myCity;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
